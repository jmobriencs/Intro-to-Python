#This program calculates the average number of wins over five years.

year1 = int(input('Enter the number of wins for year 1: '))
year2 = int(input('Enter the number of wins for year 2: '))
year3 = int(input('Enter the number of wins for year 3: '))
year4 = int(input('Enter the number of wins for year 4: '))
year5 = int(input('Enter the number of wins for year 5: '))
averageWins = (year1 + year2 + year3 + year4 + year5)/5
print ('The average number of wins over a five-year period: ', averageWins)
